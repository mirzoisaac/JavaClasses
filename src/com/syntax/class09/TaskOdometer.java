package com.syntax.class09;

public class TaskOdometer {

	public static void main(String[] args) {
		for (int i = 0; i <= 9; i++) {
			for (int j = 0; j <= 9; j++) {
				for (int x = 0; x <= 9; x++) {
					for (int y = 0; y <= 9; y++) {
						System.out.println(i + "|" + j + "|" + x + "|" + y);
					}
				}
			}
		}

	}

}
