package com.syntax.class09;

public class Pattern {

	public static void main(String[] args) {
		for (int i = 1; i <= 10; i++) {
			for (int j = 1; j <= 10; j++) {
				System.out.print("*");
			}
			System.out.println();
		}
		System.out.println("---------------------");

		for (int x = 1; x <= 5; x++) {
			for (int y = 1; y <= 9; y++) {
				System.out.print(y);
			}
			System.out.println();

		}

	}

}
