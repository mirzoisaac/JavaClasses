package com.syntax.class09;

public class MoreLoops {
	public static void main(String[] args) {
		
		for (int x = 5; x >= 1; x--) {
			for (int y = 5; y >= 1; y--) {
				System.out.print(y);
			}
			System.out.println();

		}
		System.out.println("---------------------");
		for (int i = 5; i >= 1; i--) {
			for (int j = 5; j >= 1; j--) {
				System.out.print(i);
			}
			System.out.println();

		}
		
	}
}
