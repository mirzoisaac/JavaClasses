package com.syntax.class01;

public class VariableValues {

	public static void main(String[] args) {
		byte va$=101;
		short md=2019;
		int dc=2020202020;
		long ny=101010101010l;
		float fl=99.101010f;
		double ca=1999.2020202020;
		char pa='@';
		boolean nc_sc=false;
		
		va$=99;
		md=2020;
		dc=2121212121;
		ny=5050505050505050l;
		fl=01.123455f;
		ca=2000.99999999999999;
		pa='%';
		nc_sc=true;
		
		System.out.println(va$);
		System.out.println(md);
		System.out.println(dc);
		System.out.println(ny);
		System.out.println(fl);
		System.out.println(ca);
		System.out.println(pa);
		System.out.println(nc_sc);
		
	}

}
