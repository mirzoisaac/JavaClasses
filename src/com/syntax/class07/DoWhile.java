package com.syntax.class07;

public class DoWhile {

	public static void main(String[] args) {
		int x = 1;

		do {
			System.out.println("Hello");
			x++;
		} while (x <= 5);
		
		System.out.println("_____________________--");
		
		int y=2;
		while(y<=6) {
			System.out.println("Hello");
			y++;
		}
	}

}
